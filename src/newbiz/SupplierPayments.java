            Object ob2 = dtm.getValueAt(jTable1.getSelectedRow(), 3);//cheque
           Object ob3 = dtm.getValueAt(jTable1.getSelectedRow(), 5);//cheque
//       
            PreparedStatement p1 = com.prepareStatement("insert into supplierpayment values('" + sid + "','" + ob0 + "','" + ob + "','" + jTextField9.getText() + "','" + jTextField10.getText() + "','" + jTextField11.getText() + "','" + ob3 + "','" + realDate + "')");
