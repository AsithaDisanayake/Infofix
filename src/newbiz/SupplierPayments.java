    public void loadsuplier() {
        try {
            Connection com = DB.conect();
            ResultSet r = com.createStatement().executeQuery("select * from supplier");
            while (r.next()) {
                jComboBox1.addItem(r.getString("fname"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadtable() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int rowcount = dtm.getRowCount();
            while (rowcount > 0) {
                dtm.removeRow(rowcount - 1);
                rowcount--;
            }

            Connection com = DB.conect();

            ResultSet r = com.createStatement().executeQuery("select supid from supplier where fname='" + jComboBox1.getSelectedItem() + "'");
            String sid = null;
            while (r.next()) {
                sid = r.getString("supid");
            }
            ResultSet r1 = com.createStatement().executeQuery("select * from supplierpaymentsummery where supid='" + sid + "'");
            while (r1.next()) {
                Vector v = new Vector();
                v.add(r1.getString("grnNo"));
                v.add(r1.getString("grntot"));
                v.add(r1.getString("cash"));
                v.add(r1.getString("cheque"));
                v.add(r1.getString("balance"));
                v.add(r1.getString("grndate"));
                v.add(r1.getString("paydate"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

