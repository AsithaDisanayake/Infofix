    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
    try {
            String cash = jTextField2.getText().trim();

            String cheque = jTextField3.getText().trim();
            if (cash == "" || cash.equals("")) {
                cash = "0.0";

            }
            if (cheque == "" || cheque.equals("")) {
                cheque = "0.0";

            }
            double credit = Double.parseDouble(jTextField1.getText());
            double balance = credit - (Double.parseDouble(cash) + Double.parseDouble(cheque));
            jTextField4.setText(balance + "");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

