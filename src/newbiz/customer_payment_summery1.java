    void load_table() {
        try {

            Connection com = DB.conect();
            ResultSet r = com.createStatement().executeQuery("select * from customer where fname='" + jComboBox1.getSelectedItem() + "'");
            String cusid = null;
            while (r.next()) {
                cusid = r.getString("cusid");
            }
            System.out.println(cusid);
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int i = dtm.getRowCount();
            while (i > 0) {
                dtm.removeRow(i - 1);
                i--;

            }

            ResultSet p = com.createStatement().executeQuery("select * from customerpaymentsummery where cusid='" + cusid + "'");

            while (p.next()) {
                Vector v = new Vector();
                v.add(p.getString("invoNo"));
                v.add(p.getString("invotot"));
                v.add(p.getString("cash"));
                v.add(p.getString("cheque"));
                v.add(p.getString("balance"));
                v.add(p.getString("paydate"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

