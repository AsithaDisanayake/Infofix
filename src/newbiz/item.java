                Connection com = DB.conect();
                PreparedStatement r = com.prepareStatement("insert into item values('" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + jTextField3.getText() + "','" + jTextField4.getText() + "','" + jTextField5.getText() + "')");
                r.executeUpdate();
