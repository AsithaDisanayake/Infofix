                PreparedStatement r1 = com.prepareStatement("insert into itemdl values('" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + jTextField3.getText() + "','" + jTextField4.getText() + "','" + jTextField6.getText() + "')");
